import math


def area(r):
    # Принимает на вход радиус r , и возвращает площадь круга данного радиуса
    return math.pi * r * r
    # r = 10 , return = 314.1592653589793


def perimeter(r):
    # Принимает на вход радиус r , и возвращает периметр круга данного радиуса
    return 2 * math.pi * r
    # r = 5, return = 31.41592653589793

