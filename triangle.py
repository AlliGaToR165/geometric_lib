def area(a, h): 
    #  Принимает длину стороны тругольника и длины высоты треугольника, проведенной к ней.
    # Возвращает площадь треугольника
    return a * h / 2 
    # a = 5, h = 4, return = 10

def perimeter(a, b, c): 
    # Принмает длины трех сторон треугольника и ворзвращает его периметр
    return a + b + c 
    # a = 5, b = 4, c = 3, return = 12
